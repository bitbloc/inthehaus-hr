function doReplace(tgt, repl) {
    if (!content.includes(tgt)) {
        console.error("COULD NOT FIND TARGET BLOCK:", tgt.slice(0, 100) + "...");
        process.exit(1);
    }
    content = content.replace(tgt, repl);
}

